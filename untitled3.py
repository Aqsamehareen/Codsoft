a=int(input("Enter 1st number:"))
b=int(input("Enter 2nd number:"))
choice = input("Enter the operation (+, -, *, /): ")
if choice=="+":
    print(a+b)
elif choice=="-":
    print(a-b)
elif choice=="*":
    print(a*b)
elif choice == "/":
    print(a/b)